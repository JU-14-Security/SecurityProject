CREATE schema TOPLISTAN;
drop table TOPLISTAN.USERS;
CREATE TABLE TOPLISTAN.USERS(
ID INTEGER not null PRIMARY KEY,
USERNAME VARCHAR(256),
PASSWORD VARCHAR(256),
ROLE VARCHAR(256)
);
DROP TABLE TOPLISTAN.LIST;
CREATE TABLE TOPLISTAN.LIST(
ID INTEGER not null PRIMARY KEY,
PRODUCT VARCHAR(256),
PRODUCT_URL VARCHAR(256),
SEK_SUM INTEGER
);
